    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        List<Trabajador> personas = new TrabajadorDAO().getTrabajadores();
        this.trabCbx.removeAllItems();
        for(Trabajador p : personas){
            this.trabCbx.addItem(p);
        }
        List<Cliente> e = new ClienteDAO().getClientes();
        this.clienteCbx.removeAllItems();
        for(Cliente p : e){
            this.clienteCbx.addItem(p);
        }
        List<Distribuidor> f = new DistribuidorDAO().getDistribuidores();
        this.distriCbx.removeAllItems();
        for(Distribuidor p : f){
            this.distriCbx.addItem(p);
        }
    }//GEN-LAST:event_formInternalFrameOpened

    private void ingresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarBtnActionPerformed
        // TODO add your handling code here:
        
        try{
            int id=Integer.parseInt(this.idTxt.getText().trim());
            int numero=Integer.parseInt(this.numTxt.getText().trim());
            Trabajador t = (Trabajador) this.trabCbx.getSelectedItem();
            Cliente y = (Cliente) this.clienteCbx.getSelectedItem();
            Distribuidor u = (Distribuidor) this.distriCbx.getSelectedItem();
            
            int idtrab = t.getRut();
            int idcli = y.getRut();
            int iddis = u.getRut();
            
            
            Telefono p = new Telefono();
            p.setId(id);
            p.setNumero(numero);
            p.setTrabajadorRut(idtrab);
            p.setClienteRut(idcli);
            p.setDistribuidorRut(iddis);
            

            new TelefonoDAO().ingresarTelefono(p);
            this.dispose();
            JOptionPane.showMessageDialog(null, "Ingresado OK");
       }catch(Exception e){
           JOptionPane.showMessageDialog(null, "Error al Ingresar un Cliente");
       }
        
    }//GEN-LAST:event_ingresarBtnActionPerformed


