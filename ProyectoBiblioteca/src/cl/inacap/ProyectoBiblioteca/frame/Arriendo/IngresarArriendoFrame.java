    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        try {
            int id = Integer.parseInt(this.idTxt.getText().trim());
            int costoTot = Integer.parseInt(this.totTxt.getText().trim());
            String fechaArr = this.fechArriTxt.getText().trim();
            String fechaDev = this.fechDevTxt.getText().trim();
            String fechaEntr = this.fechEntTxt.getText().trim();
            int diasRet = Integer.parseInt(this.diaTxt.getText().trim());
            int multar = Integer.parseInt(this.multTxt.getText().trim());
            int multa = multar + (int)((float)((multar)*0.19));
            int iva = 19;
            TipoDePago i = (TipoDePago) this.tipPagCbx.getSelectedItem();
            Trabajador t = (Trabajador) this.trabCbx.getSelectedItem();
            Cliente y = (Cliente) this.clienCbx.getSelectedItem();

            int idtrab = t.getRut();
            int idcli = y.getRut();
            int idPag = i.getIdPago();

            Arriendo p = new Arriendo();
            p.setId(id);
            p.setCostoTotal(costoTot);
            p.setFechaArri(fechaArr);
            p.setFechaDevEntr(fechaDev);
            p.setFechaDevReal(fechaEntr);
            p.setDiasRetraso(diasRet);
            p.setMulta(multa);
            p.setIva(iva);
            p.setTipoPago(idPag);
            p.setClienteRut(idcli);
            p.setTrabajadorRut(idtrab);

            new ArriendoDAO().ingresarArriendo(p);
            // 
            Libro b = (Libro) this.libCbx.getSelectedItem();
            int idLib = b.getIdSerie();
            
            DetalleArr v = new DetalleArr();
            
            v.setIdArr(id);
            v.setIdLibro(idLib);
            
            new DetalleArrDAO().ingresarDetalleArr(v);
            this.dispose();
            JOptionPane.showMessageDialog(null, "Ingresado OK");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Ingresar un Arriendo");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

