    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        List<TipoDePago> f = new TipoDePagoDAO().getTiposPagos();
        this.tipPagCbx.removeAllItems();
        for (TipoDePago p : f) {
            this.tipPagCbx.addItem(p);
        }
        List<Cliente> s = new ClienteDAO().getClientes();
        this.clienCbx.removeAllItems();
        for (Cliente p : s) {
            this.clienCbx.addItem(p);
        }
        List<Trabajador> d = new TrabajadorDAO().getTrabajadores();
        this.trabCbx.removeAllItems();
        for (Trabajador p : d) {
            this.trabCbx.addItem(p);
        }
        List<Libro> h = new LibroDAO().getLibros();
        this.libCbx.removeAllItems();
        for (Libro p : h) {
            this.libCbx.addItem(p);
        }
    }//GEN-LAST:event_formInternalFrameOpened

