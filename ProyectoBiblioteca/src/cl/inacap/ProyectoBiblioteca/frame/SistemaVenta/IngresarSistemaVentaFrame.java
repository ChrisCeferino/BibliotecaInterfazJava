    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:
        List<Cliente> clins = new ClienteDAO().getClientes();
        this.clienCbx.removeAllItems();
        for (Cliente p : clins) {
            this.clienCbx.addItem(p);
        }
        List<Libro> s = new LibroDAO().getLibros();
        this.libroCbx.removeAllItems();
        for (Libro p : s) {
            this.libroCbx.addItem(p);
        }
        List<Trabajador> f = new TrabajadorDAO().getTrabajadores();
        this.trabCbx.removeAllItems();
        for (Trabajador p : f) {
            this.trabCbx.addItem(p);
        }
        List<TipoDePago> e = new TipoDePagoDAO().getTiposPagos();
        this.metPagCbx.removeAllItems();
        for (TipoDePago p : e) {
            this.metPagCbx.addItem(p);
        }
        List<Boleta> bols = new BoletaDAO().getBoletas();
        for (Boleta p : bols) {
                this.nroBoletaTxt.setText((p.getFolio()+1)+"");
            }
        
 
    }//GEN-LAST:event_formInternalFrameOpened

