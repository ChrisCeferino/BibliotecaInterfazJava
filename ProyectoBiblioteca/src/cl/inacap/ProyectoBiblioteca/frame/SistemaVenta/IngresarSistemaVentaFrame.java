    private void VenderBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_VenderBtnActionPerformed
        // TODO add your handling code here:

        try {
            List<Boleta> bols = new BoletaDAO().getBoletas();
            List<Venta> vens = new VentaDAO().getVentas();
            int idBoleta = 0, idVenta = 0;
            for (Boleta p : bols) {
                idBoleta = p.getFolio() + 1;
            }
            for (Venta p : vens) {
                idVenta = p.getId() + 1;
            }
            //BOLETA
            Libro k = (Libro) this.libroCbx.getSelectedItem();
            int precioNeto = k.getPrecio();
            int precIva = precioNeto + (int) ((float) ((precioNeto) * 0.19));
            int costIva = (int) ((float) ((precioNeto) * 0.19));
            String fecVen = this.fechTxt.getText().trim();
            String hora = this.horaTxt.getText().trim();
            int iva = 19;
            TipoDePago s = (TipoDePago) this.metPagCbx.getSelectedItem();
            int idTip = s.getIdPago();

            Boleta p = new Boleta();
            p.setFolio(idBoleta);
            p.setPrecioNeto(precioNeto);
            p.setPrecioIVA(precIva);
            p.setCostoIVA(costIva);
            p.setFechaVenta(fecVen);
            p.setHoraVenta(hora);
            p.setIva(iva);
            p.setTipoPagoID(idTip);

            new BoletaDAO().ingresarBoleta(p);

            //Venta
            Trabajador y = (Trabajador) this.trabCbx.getSelectedItem();
            Cliente cl = (Cliente) this.clienCbx.getSelectedItem();
            int idTrab= y.getRut();
            int idClie = cl.getRut();

            Venta v = new Venta();
            v.setId(idVenta);
            v.setFolioBoleta(idBoleta);
            v.setClienteRut(idClie);
            v.setTrabajadorRut(idTrab);
            
            new VentaDAO().ingresarVenta(v);
            //LibroVenta
            int idLib = k.getIdSerie();
            LibroVenta h =new LibroVenta();
            h.setIdLibro(idLib);
            h.setIdVenta(idVenta);
            
            new LibroVentaDAO().ingresarLibroVenta(h);

            
            this.dispose();
            JOptionPane.showMessageDialog(null, "Ingresado OK");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Ingresar una boleta");
        }


    }//GEN-LAST:event_VenderBtnActionPerformed

