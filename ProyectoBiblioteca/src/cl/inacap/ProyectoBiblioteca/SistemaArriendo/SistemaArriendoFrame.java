    private void arrBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_arrBtnActionPerformed
        // TODO add your handling code here:

        Libro l = (Libro) this.libCbx.getSelectedItem();
        int prec = l.getPrecio();
        int costoArrDia = (int) ((float) ((prec) * 0.05));
        this.arrTotTxt.setText(costoArrDia + "");

        try {
            List<Arriendo> bols = new ArriendoDAO().getArriendos();
            int idArr = Integer.parseInt(this.nroArriTxt.getText().trim());
            String fechArr = this.fechaArrTxt.getText().trim();
            String fechDev = this.fechaDevEnTxt.getText().trim();
            String fechaRea = this.fechDevReTxt.getText().trim();
            // total costo
            int diaArr = Integer.parseInt(this.diasArrTxt.getText().trim());
            int diaRetr = Integer.parseInt(this.diaRetrTxt.getText().trim());

            int costoTot = 0, cosArr = 0, cosMul = 0, multa = 0;
            cosArr = diaArr * costoArrDia;
            multa = diaRetr * 500;
            costoTot = cosArr + multa;

            TipoDePago g = (TipoDePago) this.tipPagCbx.getSelectedItem();
            Cliente cli = (Cliente) this.cliCbx.getSelectedItem();
            Trabajador trab = (Trabajador) this.trabCbx.getSelectedItem();

            int idpag = g.getIdPago();
            int rutCli = cli.getRut();
            int rutTrab  = trab.getRut();
            
            int iva = 19;


            //Arriendo
            

            Arriendo p = new Arriendo();
            p.setId(idArr);
            p.setCostoTotal(costoTot);
            p.setFechaArri(fechArr);
            p.setFechaDevEntr(fechDev);
            p.setFechaDevReal(fechaRea);
            p.setDiasRetraso(diaRetr);
            p.setMulta(multa);
            p.setIva(iva);
            p.setTipoPago(idpag);
            p.setClienteRut(rutCli);
            p.setTrabajadorRut(rutTrab);
            

            new ArriendoDAO().ingresarArriendo(p);
            
            //DetalleArr
            DetalleArr t = new DetalleArr();
            int idlib= l.getIdSerie();
            t.setIdArr(idArr);
            t.setIdLibro(idlib);
            
            new DetalleArrDAO().ingresarDetalleArr(t);
            this.dispose();
            JOptionPane.showMessageDialog(null, "Ingresado OK");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Ingresar una arriendo");
        }


    }//GEN-LAST:event_arrBtnActionPerformed


