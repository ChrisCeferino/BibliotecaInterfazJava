    private void formInternalFrameOpened(javax.swing.event.InternalFrameEvent evt) {//GEN-FIRST:event_formInternalFrameOpened
        // TODO add your handling code here:

        List<Libro> personas = new LibroDAO().getLibros();
        this.libCbx.removeAllItems();
        for (Libro p : personas) {
            this.libCbx.addItem(p);
        }
        List<TipoDePago> s = new TipoDePagoDAO().getTiposPagos();
        this.tipPagCbx.removeAllItems();
        for (TipoDePago p : s) {
            this.tipPagCbx.addItem(p);
        }
        List<Cliente> d = new ClienteDAO().getClientes();
        this.cliCbx.removeAllItems();
        for (Cliente p : d) {
            this.cliCbx.addItem(p);
        }
        List<Trabajador> f = new TrabajadorDAO().getTrabajadores();
        this.trabCbx.removeAllItems();
        for (Trabajador p : f) {
            this.trabCbx.addItem(p);
        }
        List<Arriendo> bols = new ArriendoDAO().getArriendos();
        for (Arriendo p : bols) {
            this.nroArriTxt.setText((p.getId() + 1) + "");
        }


    }//GEN-LAST:event_formInternalFrameOpened

